function wrapDocument(content: string): string {
  return `
    <!DOCTYPE html>
    <html>
    <head>
      <meta charset="UTF-8">
      <style>
        @import url('https://fonts.googleapis.com/css2?family=IBM+Plex+Sans:wght@400;500;600;700&family=IBM+Plex+Mono:wght@400;500&family=Source+Serif+4:wght@400;600;700&display=swap');
        * { margin: 0; padding: 0; box-sizing: border-box; }
        body { font-family: 'IBM Plex Sans', -apple-system, sans-serif; font-size: 12px; color: #333; line-height: 1.5; }
        @media print {
          body { -webkit-print-color-adjust: exact; print-color-adjust: exact; }
          @page { margin: 0; size: A4; }
        }
      </style>
    </head>
    <body>
      ${content}
    </body>
    </html>
  `;
}

export function openPDFPreview(htmlContent: string, title: string): void {
  const printWindow = window.open("", "_blank");
  if (!printWindow) {
    alert("Please allow popups to generate PDFs");
    return;
  }
  printWindow.document.write(htmlContent);
  printWindow.document.close();
  printWindow.document.title = title;
  // Auto-trigger print dialog after a brief delay for rendering
  setTimeout(() => {
    printWindow.print();
  }, 500);
}

