export function generateQuotePDF(quote: Quote, workspace: Workspace, customer: Customer): string {
  const approvalReqs = getApprovalRequirements(quote.gpPercent, quote.palletVolume);

  return wrapDocument(`
    ${generateHeader("Commercial Quote", `${customer.name} — ${workspace.title}`, `QT-${quote.id.toUpperCase()}-V${quote.version}`)}
    <div style="padding:32px;">
      ${generateSection("Quote Summary", generateKVGrid([
        { label: "Customer", value: customer.name },
        { label: "Workspace", value: workspace.title },
        { label: "Region", value: customer.region },
        { label: "Quote Version", value: `v${quote.version}` },
        { label: "Status", value: quote.state.toUpperCase() },
        { label: "Date", value: quote.createdAt },
      ]))}

      ${generateSection("Pricing Schedule", generateTable(
        ["Service", "Rate", "Volume", "Monthly Revenue"],
        [
          ["Storage (per pallet/day)", `SAR ${quote.storageRate}`, `${quote.palletVolume.toLocaleString()} pallets`, formatSAR(quote.storageRate * quote.palletVolume * 30)],
          ["Inbound Handling", `SAR ${quote.inboundRate}/pallet`, `${quote.palletVolume.toLocaleString()}`, formatSAR(quote.inboundRate * quote.palletVolume)],
          ["Outbound Handling", `SAR ${quote.outboundRate}/pallet`, `${quote.palletVolume.toLocaleString()}`, formatSAR(quote.outboundRate * quote.palletVolume)],
        ]
      ))}

      ${generateSection("Financial Summary", generateKVGrid([
        { label: "Monthly Revenue", value: formatSAR(quote.monthlyRevenue) },
        { label: "Annual Revenue", value: formatSAR(quote.annualRevenue) },
        { label: "Total Cost", value: formatSAR(quote.totalCost) },
        { label: "Gross Profit", value: formatSAR(quote.gpAmount) },
        { label: "GP%", value: formatPercent(quote.gpPercent) },
        { label: "Pallet Volume", value: quote.palletVolume.toLocaleString() },
      ]))}

      ${generateSection("Approval Requirements", `
        <div style="font-size:11px;color:#555;margin-bottom:12px;">Based on GP% of ${formatPercent(quote.gpPercent)} and volume of ${quote.palletVolume.toLocaleString()} pallets:</div>
        ${generateTable(
          ["Role", "Type", "Status"],
          approvalReqs.map(r => [getRoleLabel(r.role), r.type === "feasibility" ? "Feasibility Confirmation" : "Full Approval", "Required"])
        )}
      `)}

      ${generateSection("Terms & Conditions", `
        <div style="font-size:11px;color:#555;line-height:1.6;">
          <p>1. All rates are quoted in Saudi Riyals (SAR) and are exclusive of VAT (15%).</p>
          <p>2. Storage rates are calculated on a per-pallet-per-day basis.</p>
          <p>3. This quote is valid for 30 days from the date of issue.</p>
          <p>4. Payment terms: Net 30 days from invoice date.</p>
          <p>5. Minimum contract period: 12 months.</p>
          <p>6. Volume commitments are subject to facility capacity confirmation.</p>
        </div>
      `)}
    </div>
    ${generateFooter("Quote Document")}
  `);
}

