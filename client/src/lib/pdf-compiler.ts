const HALA_NAVY = "#1a2744";
const HALA_ACCENT = "#2a4a7f";
const HALA_LIGHT = "#f8f9fb";
const HALA_BORDER = "#e2e5ea";

function generateHeader(title: string, subtitle: string, docRef: string): string {
  return `
    <div style="display:flex;align-items:center;justify-content:space-between;padding:24px 32px;background:${HALA_NAVY};color:white;margin-bottom:0;">
      <div>
        <div style="font-size:10px;letter-spacing:2px;text-transform:uppercase;opacity:0.7;margin-bottom:4px;">HALA SUPPLY CHAIN SERVICES</div>
        <div style="font-size:22px;font-weight:700;font-family:'Source Serif 4',Georgia,serif;">${title}</div>
        <div style="font-size:12px;opacity:0.8;margin-top:4px;">${subtitle}</div>
      </div>
      <div style="text-align:right;">
        <div style="font-size:28px;font-weight:700;font-family:'Source Serif 4',Georgia,serif;letter-spacing:-1px;">H</div>
        <div style="font-size:9px;opacity:0.6;margin-top:2px;">${docRef}</div>
        <div style="font-size:9px;opacity:0.6;">${new Date().toLocaleDateString("en-GB", { day: "numeric", month: "long", year: "numeric" })}</div>
      </div>
    </div>
  `;
}

function generateFooter(pageLabel: string): string {
  return `
    <div style="display:flex;align-items:center;justify-content:space-between;padding:12px 32px;border-top:1px solid ${HALA_BORDER};font-size:9px;color:#888;margin-top:auto;">
      <span>CONFIDENTIAL — Hala Supply Chain Services</span>
      <span>${pageLabel}</span>
      <span>Generated by Hala Commercial Engine</span>
    </div>
  `;
}

function generateSection(title: string, content: string): string {
  return `
    <div style="margin-bottom:24px;">
      <div style="font-size:14px;font-weight:700;font-family:'Source Serif 4',Georgia,serif;color:${HALA_NAVY};padding-bottom:8px;border-bottom:2px solid ${HALA_NAVY};margin-bottom:12px;">${title}</div>
      ${content}
    </div>
  `;
}

function generateTable(headers: string[], rows: string[][]): string {
  return `
    <table style="width:100%;border-collapse:collapse;font-size:11px;margin-bottom:16px;">
      <thead>
        <tr style="background:${HALA_LIGHT};">
          ${headers.map(h => `<th style="text-align:left;padding:8px 12px;border-bottom:2px solid ${HALA_BORDER};font-size:10px;text-transform:uppercase;letter-spacing:0.5px;color:#666;">${h}</th>`).join("")}
        </tr>
      </thead>
      <tbody>
        ${rows.map((row, i) => `
          <tr style="background:${i % 2 === 0 ? "white" : HALA_LIGHT};">
            ${row.map(cell => `<td style="padding:8px 12px;border-bottom:1px solid ${HALA_BORDER};">${cell}</td>`).join("")}
          </tr>
        `).join("")}
      </tbody>
    </table>
  `;
}

function generateKVGrid(items: { label: string; value: string }[], cols: number = 3): string {
  return `
    <div style="display:grid;grid-template-columns:repeat(${cols},1fr);gap:12px;margin-bottom:16px;">
      ${items.map(item => `
        <div style="padding:12px;background:${HALA_LIGHT};border-radius:6px;border:1px solid ${HALA_BORDER};">
          <div style="font-size:9px;text-transform:uppercase;letter-spacing:0.5px;color:#888;margin-bottom:4px;">${item.label}</div>
          <div style="font-size:14px;font-weight:600;color:${HALA_NAVY};font-family:'IBM Plex Mono',monospace;">${item.value}</div>
        </div>
      `).join("")}
    </div>
  `;
}

