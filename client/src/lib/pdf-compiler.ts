export function generatePnLPDF(workspace: Workspace, customer: Customer, quote: Quote): string {
  const annualCost = quote.totalCost;
  const annualRevenue = quote.annualRevenue;
  const grossProfit = annualRevenue - annualCost;
  const facilityCost = annualCost * 0.35;
  const staffCost = annualCost * 0.30;
  const mheCost = annualCost * 0.10;
  const insuranceCost = annualCost * 0.05;
  const opsCost = annualCost * 0.10;
  const gaCost = annualCost * 0.10;

  return wrapDocument(`
    ${generateHeader("Profit & Loss Summary", `${customer.name} — ${workspace.title}`, `PNL-${workspace.id.toUpperCase()}`)}
    <div style="padding:32px;">
      ${generateSection("Deal Overview", generateKVGrid([
        { label: "Customer", value: customer.name },
        { label: "Facility", value: customer.facility },
        { label: "Service Type", value: customer.serviceType },
        { label: "Pallet Volume", value: quote.palletVolume.toLocaleString() },
        { label: "Contract Value", value: formatSAR(annualRevenue) },
        { label: "GP%", value: formatPercent(quote.gpPercent) },
      ]))}

      ${generateSection("Revenue Breakdown", generateTable(
        ["Revenue Line", "Monthly", "Annual", "% of Total"],
        [
          ["Storage Revenue", formatSAR(quote.storageRate * quote.palletVolume * 30), formatSAR(quote.storageRate * quote.palletVolume * 30 * 12), `${((quote.storageRate * quote.palletVolume * 30 * 12 / annualRevenue) * 100).toFixed(1)}%`],
          ["Inbound Handling", formatSAR(quote.inboundRate * quote.palletVolume), formatSAR(quote.inboundRate * quote.palletVolume * 12), `${((quote.inboundRate * quote.palletVolume * 12 / annualRevenue) * 100).toFixed(1)}%`],
          ["Outbound Handling", formatSAR(quote.outboundRate * quote.palletVolume), formatSAR(quote.outboundRate * quote.palletVolume * 12), `${((quote.outboundRate * quote.palletVolume * 12 / annualRevenue) * 100).toFixed(1)}%`],
          ["<strong>Total Revenue</strong>", `<strong>${formatSAR(quote.monthlyRevenue)}</strong>`, `<strong>${formatSAR(annualRevenue)}</strong>`, "<strong>100%</strong>"],
        ]
      ))}

      ${generateSection("Cost Breakdown", generateTable(
        ["Cost Category", "Annual Cost", "% of OPEX"],
        [
          ["Facility Rent & Utilities", formatSAR(facilityCost), "35%"],
          ["Staff & Labor", formatSAR(staffCost), "30%"],
          ["MHE (Purchase/Rental)", formatSAR(mheCost), "10%"],
          ["Insurance", formatSAR(insuranceCost), "5%"],
          ["Operational Costs", formatSAR(opsCost), "10%"],
          ["G&A (10% of OPEX)", formatSAR(gaCost), "10%"],
          ["<strong>Total OPEX</strong>", `<strong>${formatSAR(annualCost)}</strong>`, "<strong>100%</strong>"],
        ]
      ))}

      ${generateSection("Profitability Summary", generateKVGrid([
        { label: "Annual Revenue", value: formatSAR(annualRevenue) },
        { label: "Total OPEX", value: formatSAR(annualCost) },
        { label: "Gross Profit", value: formatSAR(grossProfit) },
        { label: "GP%", value: formatPercent(quote.gpPercent) },
        { label: "Monthly Revenue", value: formatSAR(quote.monthlyRevenue) },
        { label: "Monthly Profit", value: formatSAR(grossProfit / 12) },
      ], 3))}

      <div style="padding:16px;background:#fff8e1;border:1px solid #ffe082;border-radius:6px;font-size:11px;color:#795548;margin-top:16px;">
        <strong>Approval Note:</strong> GP% of ${formatPercent(quote.gpPercent)} ${quote.gpPercent >= 22 ? "meets the standard threshold." : quote.gpPercent >= 10 ? "is below the 22% threshold — Director approval required." : "is below 10% — CEO/CFO approval required."}
      </div>
    </div>
    ${generateFooter("P&L Summary")}
  `);
}

export function generateECRScorecardPDF(customer: Customer): string {
  const growthScore = customer.grade === "A" ? 0.8 : customer.grade === "B" ? 0.6 : customer.grade === "C" ? 0.4 : 0.2;
  const acquisitionScore = customer.grade === "A" ? 0.9 : customer.grade === "B" ? 0.7 : customer.grade === "C" ? 0.5 : 0.3;
  const ecr = calculateECR(customer.dso, customer.contractValue2025, 25, growthScore, acquisitionScore);

  let dsoScore = 0;
  if (customer.dso <= 30) dsoScore = 1.0;
  else if (customer.dso <= 45) dsoScore = 0.5;
  else dsoScore = 0.0;

  let cvScore = 0;
  if (customer.contractValue2025 > 2000000) cvScore = 1.0;
  else if (customer.contractValue2025 > 1000000) cvScore = 0.7;
  else if (customer.contractValue2025 > 500000) cvScore = 0.3;
  else if (customer.contractValue2025 > 100000) cvScore = 0.2;
  else cvScore = 0.0;

  return wrapDocument(`
    ${generateHeader("Customer Classification Scorecard", customer.name, `ECR-${customer.code}`)}
    <div style="padding:32px;">
      ${generateSection("Customer Profile", generateKVGrid([
        { label: "Customer Name", value: customer.name },
        { label: "Code", value: customer.code },
        { label: "Group", value: customer.group },
        { label: "Region", value: `${customer.city}, ${customer.region}` },
        { label: "Industry", value: customer.industry },
        { label: "Status", value: customer.status },
        { label: "Service Type", value: customer.serviceType },
        { label: "Account Owner", value: customer.accountOwner },
        { label: "Facility", value: customer.facility },
      ]))}

      ${generateSection("ECR Score Breakdown", generateTable(
        ["Criteria", "Weight", "Raw Value", "Score", "Weighted"],
        [
          ["DSO (Days Sales Outstanding)", "30%", `${customer.dso} days`, dsoScore.toFixed(1), (dsoScore * 0.30 * 100).toFixed(1) + "%"],
          ["Contract Value", "30%", formatSAR(customer.contractValue2025), cvScore.toFixed(1), (cvScore * 0.30 * 100).toFixed(1) + "%"],
          ["Client Profitability (GP%)", "20%", "Calculated", "—", "—"],
          ["Potential Growth", "10%", "Assessed", growthScore.toFixed(1), (growthScore * 0.10 * 100).toFixed(1) + "%"],
          ["Cost of Acquisition", "10%", "Assessed", acquisitionScore.toFixed(1), (acquisitionScore * 0.10 * 100).toFixed(1) + "%"],
          [`<strong>Total ECR Score</strong>`, "<strong>100%</strong>", "", "", `<strong>${ecr.score.toFixed(1)}%</strong>`],
        ]
      ))}

      <div style="text-align:center;padding:24px;margin:16px 0;">
        <div style="display:inline-block;width:80px;height:80px;border-radius:50%;background:${ecr.grade === "A" ? "#22c55e" : ecr.grade === "B" ? "#3b82f6" : ecr.grade === "C" ? "#f59e0b" : ecr.grade === "D" ? "#f97316" : "#ef4444"};color:white;font-size:36px;font-weight:700;line-height:80px;font-family:'Source Serif 4',Georgia,serif;">${ecr.grade}</div>
        <div style="font-size:14px;font-weight:600;margin-top:8px;color:${HALA_NAVY};">Grade: ${ecr.grade} (${ecr.score.toFixed(1)}%)</div>
      </div>

      ${generateSection("Financial Performance", generateKVGrid([
        { label: "Revenue 2023", value: formatSAR(customer.revenue2023) },
        { label: "Revenue 2024", value: formatSAR(customer.revenue2024) },
        { label: "Revenue 2025 YTD", value: formatSAR(customer.revenue2025) },
        { label: "Contract Value 2025", value: formatSAR(customer.contractValue2025) },
        { label: "DSO", value: `${customer.dso} days` },
        { label: "Payment Status", value: customer.paymentStatus },
      ]))}

      ${generateSection("Capacity Utilization", generateKVGrid([
        { label: "Pallets Contracted", value: customer.palletContracted.toLocaleString() },
        { label: "Pallets Occupied", value: customer.palletOccupied.toLocaleString() },
        { label: "Utilization Rate", value: customer.palletContracted > 0 ? formatPercent((customer.palletOccupied / customer.palletContracted) * 100) : "N/A" },
        { label: "Growth Potential", value: customer.palletPotential.toLocaleString() + " pallets" },
        { label: "Rate Per Pallet", value: `SAR ${customer.ratePerPallet}/day` },
        { label: "Contract Expiry", value: customer.contractExpiry },
      ]))}

      ${generateSection("Grade Scale Reference", generateTable(
        ["Grade", "Score Range", "Description"],
        [
          ["A", "90–100%", "Premium client — strategic priority, full service commitment"],
          ["B", "80–89%", "Strong client — maintain and grow relationship"],
          ["C", "70–79%", "Standard client — monitor performance, seek improvement"],
          ["D", "60–69%", "At-risk client — review terms, consider restructuring"],
          ["F", "Below 60%", "Underperforming — escalate for management review"],
        ]
      ))}
    </div>
    ${generateFooter("ECR Scorecard")}
  `);
}

